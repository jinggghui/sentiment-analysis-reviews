def extract_citations(text):
    """
    Extract all [Review #XXXX] citations from text using regex

    Args:
        text: LLM response with citations

    Returns:
        List of review IDs found in citations
    """

    # Regex pattern: [Review #123]
    # \[ = literal [
    # Review # = literal text
    # (\d+) = capture one or more digits
    # \] = literal ]
    pattern = r'\[Review #(\d+)\]'

    # Find all matches
    matches = re.findall(pattern, text)

    # Convert to integers
    citation_ids = [int(m) for m in matches]

    return citation_ids

