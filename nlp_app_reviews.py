def retrieve_reviews(query, top_k=5):
  print(f"Query: {query}")

  # BM25 search (keyword matching)
  query_tokens = query.lower().split()
  bm25_scores = bm25.get_scores(query_tokens)
  print(f"BM25 scored: {bm25_scores}")

  # Convert query into embeddings
  query_embedding = model.encode(query, convert_to_numpy=True)
  # print(f"Query embedding: {query_embedding}")

  # Get magnitude
  query_norm = np.linalg.norm(query_embedding)
  reviews_norm = np.linalg.norm(review_embeddings, axis=1)
  print(f"Query norm: {query_norm}")
  print(f"Reviews norm: {reviews_norm}, shape={reviews_norm.shape}")

  # Calculate cosine similarity
  # Cosine similarity = dot product / (norm1 * norm2)
  dot_product = np.dot(review_embeddings, query_embedding)
  cosine_similarities = dot_product / (query_norm * reviews_norm)
  print(f"Dot product: {dot_product}")
  print(f"Cosine similarities: {cosine_similarities}")

  # Normalize
  normalized_cosine_sim = (cosine_similarities + 1) / 2
  print(f"Normalized cosine similarities: {normalized_cosine_sim}")

  max_bm25 = np.max(bm25_scores)
  if max_bm25 > 0:
    bm25_normalized = bm25_scores / max_bm25
  else:
    bm25_normalized = bm25_scores

  combined_scores = bm25_scores * 0.4 + normalized_cosine_sim * 0.6

  # Rank them by scores, return indicess
  top_indices = np.argsort(combined_scores)[::-1][:top_k]

  for rank, index in enumerate(top_indices):
    print(f"\n[Rank {rank}] Review ID: {review_ids[index]}")
    print(f"  Rating: {review_ratings[index]}⭐")
    print(f"  Date: {review_dates[index]}")
    print(f"  BM25 Score: {bm25_normalized[index]:.3f}")
    print(f"  Cosine Similarity Score: {normalized_cosine_sim[index]:.3f}")
    print(f"  Combined Score: {combined_scores[index]:.3f}")
    print(f"  Text: {review_texts[index][:100]}...")

  return top_indices

